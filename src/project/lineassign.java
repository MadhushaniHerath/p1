   public void fillTable(){
        
        String sql = "";
        try{

            sql = "select line_assign_id,machine_id,line_id from line_assign where deleted = 0 ;";
            

            ResultSet rs = (ResultSet)Database.getData(sql);
        
            int i = 0;
            rs.last();
            i = rs.getRow();
            
            Object[][] scheduleData = new Object[i][3];

            i = 0;
            
            rs.beforeFirst();

            while(rs.next()){
                scheduleData[i][0] = rs.getString("line_assign_id");
                scheduleData[i][1] = rs.getString("machine_id");
                scheduleData[i][2] = rs.getString("line_id");
                
                i++;
            }

            jTable1.setModel(new javax.swing.table.DefaultTableModel(
                scheduleData,
                new String [] {
                    "Assign ID","Machine ID","Line ID"
                }){
                    boolean[] canEdit = new boolean [] {
                        false, false, false
                    };

                    public boolean isCellEditable(int rowIndex, int columnIndex) {
                        return canEdit [columnIndex];
                    }
                });
            rs.close();
        }catch(Exception e){
            System.out.println(e);
        }
    }


